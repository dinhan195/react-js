/** @format */

function maxNumber(num1, num2) {
  if (num1 > num2) {
    return num1;
  } else {
    return num2;
  }
}

console.log("Max Number: ", maxNumber(10, 6));
console.log("Max Number: ", maxNumber(40, 100));
console.log("Max Number: ", maxNumber(50, 30));
console.log("Max Number: ", maxNumber(1, 2));
